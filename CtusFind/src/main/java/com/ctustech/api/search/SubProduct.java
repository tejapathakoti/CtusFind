package com.ctustech.api.search;

public class SubProduct {

    private String setAlertNumber;
    private String setName;
    private String setRiskType;
    private String setYear;
    private String id;
    private String year;

    public String getSetAlertNumber() {
        return setAlertNumber;
    }

    public void setSetAlertNumber(String setAlertNumber) {
        this.setAlertNumber = setAlertNumber;
    }

    public String getSetName() {
        return setName;
    }

    public void setSetName(String setName) {
        this.setName = setName;
    }

    public String getSetRiskType() {
        return setRiskType;
    }

    public void setSetRiskType(String setRiskType) {
        this.setRiskType = setRiskType;
    }

    public String getSetYear() {
        return setYear;
    }

    public void setSetYear(String setYear) {
        this.setYear = setYear;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
